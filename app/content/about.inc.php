<div id="title_header"><span class="title_text">About peachy.ws</span></div>
<br />
<br />
<b>peachy.ws</b> is a highly experimental attempt at creating a "command-line" interface for accessing web applications, edited via the community in the style of a wiki.
<br /><br />
Inspired by <a href="http://www.yubnub.org">yubnub</a>. 
<br /><br />
Written using:<br />
php, mysql, javascript, mod_rewrite goodness, red-bull